def test_extract_all_constants_from_synthetic_measured_csvs(tmp_path):
    for dtype, tflops in {"fp16": 280.0, "int8": 560.0, "bf16": 280.0}.items():
        _write_rows(tmp_path / f"cube_peak_{dtype}.csv", "cube", _cube_duration_for(tflops))

    for op in ["add", "mul", "max", "min"]:
        _write_rows(tmp_path / f"vector_peak_elemwise_{op}.csv", f"vector_{op}", _vector_duration_for(640.0))
    _write_rows(tmp_path / "vector_peak_transcendental.csv", "vector", _vector_duration_for(160.0))

    for src, dst, gbps in [
        ("gm", "ub", 180.0),
        ("ub", "gm", 175.0),
        ("gm", "l1", 190.0),
        ("l1", "l0a", 350.0),
    ]:
        _write_rows(tmp_path / f"mte_{src}_to_{dst}.csv", "mte", _mte_duration_for(gbps), n_runs=45)

    intercept_us = 2000.0 / 1850.0
    for k_value in [128, 256, 384, 512, 1024, 2048]:
        _write_rows(
            tmp_path / f"mandatory_handoff_K{k_value}.csv",
            "handoff",
            intercept_us + 0.002 * k_value,
        )

    db = extract_all_constants(tmp_path)

    violations = db.validate_p0_constants()
    assert violations == [
        "BW_l0c_to_gm_sustained: missing",
        "BW_hbm_allcore_sustained: missing",
    ]
    assert db.cube.throughput[DType.FP16] == pytest.approx(280.0)
    assert db.cube.throughput[DType.INT8] == pytest.approx(560.0)
    assert db.get_constant("P_vector_add_sustained").source == "cce_microbench"
    assert abs(db.mandatory_handoff_cycles - 2000.0) < 1e-6

    csv_path = tmp_path / "bandwidth_910b3.csv"
    write_bandwidth_csv(db, csv_path)
    assert "gm,ub,-1,-1,sustained,180" in csv_path.read_text()


