def _write_rows(path: Path, op_name: str, duration_us: float, n_runs: int = 30) -> None:
    path.write_text(
        "op_name,op_type,duration(us),cycles,task_id,core_id\n"
        + "\n".join(
            f"{op_name},bench,{duration_us:.9f},{duration_us * 1850:.3f},{idx},0"
            for idx in range(n_runs)
        )
        + "\n"
    )


def _cube_duration_for(tflops: float) -> float:
    total_flops = 2 * 128 * 128 * 4096 * 30
    return total_flops / (tflops * 1e6)


def _vector_duration_for(gflops: float) -> float:
    total_ops = 256 * 10000
    return total_ops / (gflops * 1e3)


def _mte_duration_for(gbps: float) -> float:
    total_bytes = 131072 * 2 * 1280
    return total_bytes / (gbps * 1000.0)


